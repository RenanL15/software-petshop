CREATE DATABASE CAOAMIGO;

#DROP DATABASE CAOAMIGO;

USE CAOAMIGO;

CREATE TABLE IF NOT EXISTS TB_TUTORES (
    CD_TUTOR INT NOT NULL AUTO_INCREMENT,
    NM_TUTOR VARCHAR(80),
    CPF INT(11),
	PRIMARY KEY (CD_TUTOR)
);

CREATE TABLE IF NOT EXISTS TB_PETS (
    CD_PET INT NOT NULL AUTO_INCREMENT,
    NM_PET VARCHAR(80),
    IDADE INT,
    RACA VARCHAR(80),
    NM_TUTOR VARCHAR(80),
	PRIMARY KEY (CD_PET)
);

#DROP TABLE TB_PETS;

CREATE TABLE IF NOT EXISTS TB_AGENDAMENTOS (
    CD_AGENDAMENTO INT NOT NULL AUTO_INCREMENT,
    NM_TIPO VARCHAR(50),
    NM_PET VARCHAR(50),
    DT_AGENDAMENTO VARCHAR(50),
	PRIMARY KEY (CD_AGENDAMENTO)
);

#DROP TABLE TB_AGENDAMENTOS;

CREATE TABLE IF NOT EXISTS TB_USUARIOS (
    CD_USUARIO INT NOT NULL AUTO_INCREMENT,
    NM_USUARIO VARCHAR(80),
    SENHA VARCHAR(80),
    PRIMARY KEY (CD_USUARIO)
);